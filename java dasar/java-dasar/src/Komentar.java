public class Komentar {
    public static void main(String[] args) {
        System.out.println(sum(10,10));
    }
    /**
     * menjumlahkan angka pertama dan angka kedua menggunakan method
     * @param value1 angka pertama
     * @param value2 angka kedua
     * @return hasil penjumlahan Value1 pertama dan Value2 kedua
     */
    static int sum(int value1, int value2)
    {
        //jumlahkan value1 ditambah dengan value2
        return value1 + value2;
    }

}
